package pkg2_javafundamental;

public class Main {
    public static void main(String[] args) {
        // Mendefinisikan panjang dan lebar persegi panjang
        double panjang = 20.5;
        double lebar = 5.0;

        // Menghitung luas persegi panjang
        double luas = panjang * lebar;

        // Menampilkan hasil luas dengan satuan pengukuran
        System.out.println("Luas Persegi Panjang Adalah: " + luas + " cm");
    }
}